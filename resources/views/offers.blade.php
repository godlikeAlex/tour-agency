            <p>Желтая лихорадка передается комарами рода "Египти", заразиться которой можно как в природных условиях,
                так и в городах. Инкубационный период, от момента заражения до первых признаков заболевания, составляет
                от 3 до 6 дней. Заболевание характеризуется высокой лихорадкой, геморрагической сыпью, поражением почек,
                печени, с развитием желтухи и острой почечной недостаточности. Течение болезни крайне тяжелое и в
                большинстве случаев имеет смертельный исход. При выезде в страны Южно-Американского и Африканского
                континентов, где требуется обязательное проведение профилактических прививок, являющихся единственной
                мерой предупреждения этого опасного заболевания, необходимо получить однократную прививку, которая
                проводится не позднее, чем за 10 дней до выезда, иммунитет сохраняется в течение 10 лет, после чего
                проводится повторная прививка. Без международного свидетельства о вакцинации против желтой лихорадки
                выезд в неблагополучные страны запрещается.</p>
            <p><b>ЛИХОРАДКИ ЭБОЛА, ЛАССА, МАРБУРГ</b></p>
            <p>Природно-очаговые инфекционные болезни, источниками возбудителей которых являются животные и грызуны, а
                также больной человек. Данные лихорадки относятся к тяжелым вирусным заболеваниям с почти одинаковой
                клинической картиной, характеризующейся высокой температурой, геморрагической сыпью, кровотечениями из
                носа, десен, появлением крови в стуле и рвотных массах, головными болями, общей слабостью, болями в
                грудной клетке и желудке. Инкубационный период при всех лихорадках составляет от 3 до 17 дней.</p>
            <p><b>ПАРАЗИТАРНЫЕ ЗАБОЛЕВАНИЯ</b></p>
            <p>Широкое распространение в тропических и субтропических странах имеют опасные для здоровья и другие
                паразитарные заболевания, заражение которыми возможно через фрукты, овощи и зелень, мясо, рыбу и
                продукты моря. Личинками некоторых глистов можно также заразиться при контакте с почвой и купании в
                пресных водоемах через неповрежденную кожу, а через укусы кровососущих насекомых - глистными
                заболеваниями, возбудители которых паразитируют в лимфатической системе, стенках полостей тела и
                подкожной клетчатке. Для многих из этих заболеваний характерны длительный инкубационный период и тяжелое
                хроническое течение.</p>
            <p><b>МАЛЯРИЯ</b></p>
            <p>Тяжелое паразитарное заболеваний, широко распространенное в странах с тропическим и субтропическим
                климатом. Заражение происходит при укусах малярийных комаров. Известны 4 формы малярии, из которых
                наиболее тяжелая - тропическая, распространенная в странах Африки. Инкубационный период составляет от 7
                дней до 1 месяца при тропической малярии и до 3-х лет при других формах. Симптомы заболевания –
                повышение температуры, озноб, сильное потоотделение, головная боль, слабость. При тропической малярии
                без проведения своевременного лечения возможен смертельный исход в очень короткий срок от начала
                заболевания. С целью профилактики необходимо регулярно принимать противомалярийные препараты. Прием
                препаратов надо начинать за 1 неделю до выезда в зарубежные страны, продолжать весь период пребывания и
                1 месяц после возвращения.</p>
            <p><b>ЧУМА</b></p>
            <p>Чума – природно-очаговая инфекция, которой болеют люди и животные, переносчиками являются блохи,
                паразитирующие на грызунах и других животных. Заражение чумой происходит при укусах инфицированными
                блохами, контакте с больными животными и грызунами, а также воздушно-капельным путем при общении с
                больным легочной чумой. Время, которое проходит с момента попадания возбудителя чумы в организм человека
                до появления первых симптомов заболевания, составляет от нескольких часов до 6 дней. Заболевание
                начинается с высокой температуры, сильного озноба, головной боли, увеличения лимфоузлов и кашля с
                кровью.
                <br>
                При появлении указанных признаков заболевания необходимо немедленно обратиться к врачу.</p>
            <p><b>ГРИПП ПТИЦ</b></p>
            <p>Грипп птиц – острое инфекционное заболевание, возбудителем которого является вирус. Заражение человека
                происходит при тесном контакте с инфицированной и мертвой домашней и дикой птицей. В ряде случаев
                возможно заражение человека при употреблении в пищу мяса и яиц больных птиц без достаточной термической
                обработки.
                <br>
                Опасны выделения зараженных птиц, которые, попадая на растения, в воздух, в воду, затем могут заразить
                человека через воду при питье и купании, а так же&nbsp; воздушно-капельным, воздушно-пылевым путем,
                и&nbsp; через грязные руки.&nbsp;<br>
                Инкубационный период составляет от нескольких часов до 5 дней. Заболевание гриппом птиц начинается остро
                с озноба, повышения температуры до 38°С и выше, мышечных и головных болей, болей в горле. Возможен
                водянистый жидкий стул, многократная рвота. Состояние быстро ухудшается. Через 2-3 дня появляется
                влажный кашель, часто с примесью крови, одышка. Затем может возникнуть затруднение дыхания. Возможно
                поражение печени, почек и мозга.&nbsp;<br>
                При появлении первых признаков заболевания необходимо срочно обратиться к врачу для установления
                диагноза и назначения адекватного и своевременного лечения, так как позднее начало лечения неизбежно
                приводит к развитию осложнений. Меры профилактики. Необходимо избегать контакта с домашней и дикой
                птицей в домашних хозяйствах, рынках и мест массового скопления птицы на открытых водоемах. Выгул
                домашней птицы должен проводиться только на частных подворьях граждан.</p>
            <p><b>СЕКСУАЛЬНЫЕ КОНТАКТЫ</b></p>
            <p>При случайных половых связях можно заразиться такими опасными инфекциями, как СПИД, сифилис, вирусный
                гепатит В и другими, передающимися половым путем заболеваниями. Эти болезни могут серьезно повлиять не
                только на Вашу жизнь, но и на жизнь Вашей семьи. Для предупреждения указанных заболеваний всегда имейте
                при себе и пользуйтесь индивидуальными средствами защиты. Избегайте сомнительных сексуальных контактов!
