            <p><b>ВИЧ-инфекция</b></p>
            <p>Хроническая медленная вирусная инфекция, скрытый период которой составляет от нескольких месяцев до
                нескольких лет. Основными путями передачи инфекции являются: половой, парентеральный (при использовании
                загрязненных кровью медицинского инструментария и шприцев при введении наркотиков), донорские органы и
                ткани от больных ВИЧ-инфекцией. Это особенно необходимо знать при выезде в страны, где еще не налажена
                система контроля за донорской кровью и существует опасность использования нестерильного медицинского
                инструментария при оказании медицинской помощи, особенно в стоматологии. Заразившись ВИЧ-инфекцией,
                человек становится вирусоносителем и, оставаясь в течение длительного времени практически здоровым,
                может инфицировать своих половых партнеров.
                <br>
                Конечной стадией течения ВИЧ-инфекции является СПИД – синдром приобретенного иммунодефицита человека,
                при котором происходит прогрессирующее разрушение иммунной системы человека, не поддающееся лечению и в
                течение года заканчивающееся смертью. Для профилактики заражения ВИЧ-инфекцией Вам необходимо
                позаботиться о запасе одноразовых шприцев и презервативов, перед выездом посетить стоматолога.</p>
